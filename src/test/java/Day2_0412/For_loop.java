package Day2_0412;

public class For_loop {

    public static void main(String[] args) {

        String[] states= new String[4];
        states[0]="New York";
        states[1]="California";
        states[2]="ohio";
        states[3]="Arizona";

        //print all the states from 0 to 3
        for(int i = 0;i<states.length; i++){
            System.out.println("my state is "+ states[i]);
        }//end of for loop










    }







}
