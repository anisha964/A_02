package Day2_0412;

public class While_loop {

    public static void main(String[] args) {

        String[] states= new String[4];
        states[0]="New York";
        states[1]="California";
        states[2]="ohio";
        states[3]="Arizona";

     //while loop is more of broken down into statements
     int i= 0;
     while(i<states.length){

         System.out.println("My state is "+ states[i]);

       i= i+1;
    }//end of while loop




}//end of main method





}//end of java class
